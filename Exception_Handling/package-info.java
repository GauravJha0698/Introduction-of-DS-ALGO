package Exception_Handling;
//Exception is an error event that can happen during the execution of program and disrupts its normal flow.
//
//Exceptions in java can arise from different kind of situations such as wrong data entered by user,hardware failure,network connection failure,Database server down etc.
//
//Java provides a robust and object oriented way to handle exception scenarios,known as Exception Handling.
//
//When the exception occurs in a method,the process of creating
//the exception object and handling it over to runtime environment is called THROWING THE EXCEPTION.
//
//If appropriate exception handler is found,exception object is
//passed to the handler to process it.The handler is said to be CATHCHING THE EXCEPTION.