package Object_Oriented;

public class Access_Modifiers {
//The Access Modifiers in Java specifies the accessibility or scope of a field,method,constructor or class.
//We can change the access level of fields,constructors,methods and class by applying the access modifier on it.

	
	
//There are four types of access modifiers:-
	//1.PRIVATE:The access level of a private modifier is only 
	//  within the class.It cannot be accessed from outside the class.
	//2.DEFAULT:The access level of default modifier within the 
	//  package.It cannot be accessed from outside the package.
	//  If you do not specify any access level,it will be default.
	//3.PROTECTED:The access level of protected modifier is within 
	//  the package and outside the package through the child class.
	//  If you donot make the child class,it cannot be accessed from outside the package.
	//4.PUBLIC:The access level of a public modifier is everywhere.
	//  It can be accessed from within the class,outside the class,within the package and outside the package.
	
}
